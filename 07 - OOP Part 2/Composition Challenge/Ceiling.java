public class Ceiling {

    private int height;
    private String paintColour;

    public Ceiling(int height, String paintColour) {
        this.height = height;
        this.paintColour = paintColour;
    }

    public int getHeight() {
        return this.height;
    }

    public String getPaintColour() {
        return this.paintColour;
    }

}
